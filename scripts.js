function multiply(number, secondNumber) {
    return number * secondNumber;
}

function addition(number, secondNumber) {
    return number + secondNumber;
}

function subtraction(number, secondNumber) {
    return number - secondNumber;
}

function division(number, secondNumber) {
    return number / secondNumber;
}


